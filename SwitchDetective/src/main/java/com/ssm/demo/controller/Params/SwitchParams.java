package com.ssm.demo.controller.Params;

public class SwitchParams {
    public String code;
    public String name;
    public String code_cabinet;
    public String statuc_orginal;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode_cabinet() {
        return code_cabinet;
    }

    public void setCode_cabinet(String code_cabinet) {
        this.code_cabinet = code_cabinet;
    }

    public String getStatuc_orginal() {
        return statuc_orginal;
    }

    public void setStatuc_orginal(String statuc_orginal) {
        this.statuc_orginal = statuc_orginal;
    }
}
